  /// Vérifie si une espèce est valide
  static bool isValidSpecies(String speciesName) {
    final normalized = speciesName.toLowerCase().trim();
    return validSpecies.contains(normalized);
  }

  /// Retourne l'espèce la plus proche en cas de typo
  static String? findClosestMatch(String input) {
    final normalized = input.toLowerCase().trim();
    if (validSpecies.contains(normalized)) return normalized;

    for (final species in validSpecies) {
      if (species.contains(normalized) || normalized.contains(species)) {
        return species;
      }
    }

    return null;
  }
